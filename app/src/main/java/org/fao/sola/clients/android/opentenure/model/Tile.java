	public static int createTiles(List<Tile> tiles){
		int result = 0;
		Connection localConnection = null;
		PreparedStatement statement = null;

		try {

			localConnection = OpenTenureApplication.getInstance().getDatabase()
					.getConnection();
			statement = localConnection
					.prepareStatement("INSERT INTO TILE (TILE_ID, URL, FILE_NAME) VALUES(?,?,?)");
			for(Tile tile:tiles){
				statement.setString(1, tile.getTileId());
				statement.setString(2, tile.getUrl());
				statement.setString(3, tile.getFileName());
				statement.addBatch();
			}
			int[] results = statement.executeBatch();
			for(int i:results){
				result+=results[i];
			}
		} catch (SQLException e) {
			e.printStackTrace();
		} catch (Exception exception) {
			exception.printStackTrace();
		} finally {
			if (statement != null) {
				try {
					statement.close();
				} catch (SQLException e) {
				}
			}
			if (localConnection != null) {
				try {
					localConnection.close();
				} catch (SQLException e) {
				}
			}
		}
		return result;
	}

